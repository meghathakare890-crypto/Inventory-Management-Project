CREATE PROCEDURE Add_Product
    @PID VARCHAR(10),
    @PDESC VARCHAR(100),
    @PRICE DECIMAL(10,2),
    @CATEGORY VARCHAR(50)
AS
BEGIN
    INSERT INTO PRODUCT
    VALUES(@PID,@PDESC,@PRICE,@CATEGORY);
END;

