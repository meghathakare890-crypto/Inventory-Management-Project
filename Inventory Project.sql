SELECT
    O.OID,
    O.ODATE,
    C.CNAME,
    P.PDESC,
    P.PRICE,
    O.OQTY,
    (P.PRICE * O.OQTY) AS AMOUNT
FROM ORDERS O
JOIN CUST C ON O.CID = C.CID
JOIN PRODUCT P ON O.PID = P.PID;