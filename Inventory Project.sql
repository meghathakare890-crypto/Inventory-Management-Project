CREATE DATABASE InventoryDB;

USE InventoryDB;

CREATE TABLE PRODUCT
(
    PID VARCHAR(10) PRIMARY KEY,
    PDESC VARCHAR(100) NOT NULL,
    PRICE DECIMAL(10,2) NOT NULL,
    CATEGORY VARCHAR(50)
);

CREATE TABLE SUPPLIER
(
    SID VARCHAR(10) PRIMARY KEY,
    SNAME VARCHAR(50) NOT NULL,
    SADD VARCHAR(100),
    SCITY VARCHAR(50),
    SPHONE VARCHAR(15) UNIQUE,
    EMAIL VARCHAR(100)
);

CREATE TABLE CUST
(
    CID VARCHAR(10) PRIMARY KEY,
    CNAME VARCHAR(50) NOT NULL,
    ADDRESS VARCHAR(100),
    CITY VARCHAR(50),
    PHONE VARCHAR(15),
    EMAIL VARCHAR(100),
    DOB DATE
);

CREATE TABLE STOCK
(
    PID VARCHAR(10) PRIMARY KEY,
    SQTY INT,
    ROL INT,
    MOQ INT,

    FOREIGN KEY (PID)
    REFERENCES PRODUCT(PID)
);

CREATE TABLE ORDERS
(
    OID VARCHAR(10) PRIMARY KEY,
    ODATE DATE,
    CID VARCHAR(10),
    PID VARCHAR(10),
    OQTY INT,

    FOREIGN KEY (CID)
    REFERENCES CUST(CID),

    FOREIGN KEY (PID)
    REFERENCES PRODUCT(PID)
);

CREATE TABLE BILL
(
    OID VARCHAR(10),
    ODATE DATE,
    CNAME VARCHAR(50),
    ADDRESS VARCHAR(100),
    PHONE VARCHAR(15),
    PDESC VARCHAR(100),
    PRICE DECIMAL(10,2),
    QTY INT,
    AMT DECIMAL(10,2)
);

